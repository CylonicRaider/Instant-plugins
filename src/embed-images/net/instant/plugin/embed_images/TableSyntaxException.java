package net.instant.plugin.embed_images;

public class TableSyntaxException extends Exception {

    public TableSyntaxException() {
        super();
    }
    public TableSyntaxException(String message) {
        super(message);
    }
    public TableSyntaxException(String message, Throwable cause) {
        super(message, cause);
    }
    public TableSyntaxException(Throwable cause) {
        super(cause);
    }

}
